package java19;

import java.util.HashMap;

public class 绊按包府 {

	public static void main(String[] args) {
		
		 HashMap mem = new HashMap();
		 
		 mem.put(100, "辫单捞");
		 mem.put(200, "辫荤傈");
		 mem.put(300, "辫备炼");
		 mem.put(400, "辫磊丰");
		 
		 System.out.println(mem);
		 
		 System.out.println("200锅 绊按篮 呕硼, 300锅狼 绊按篮 俺疙(辫面己)");
		 
		 mem.remove(200);
		 mem.put(300, "辫面己");
		 
		 System.out.println(mem);
	}

}
